export default function FooterBar() {
  const pathname = usePathname()

  // Don't show on login, form filler, or landing page
  if (pathname === '/' || pathname === '/login' || pathname.startsWith('/forms/')) return null

  return (
    <nav className="fixed bottom-0 end-0 start-0 z-50 bg-white/80 backdrop-blur-xl border-t border-gray-200/60 safe-area-bottom">
      <div className="max-w-lg mx-auto flex items-center justify-around h-16 px-4">
        {NAV_ITEMS.map(item => {
          const isActive = pathname === item.href || pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center gap-0.5 px-6 py-1 rounded-2xl transition-all duration-200 ${
                 isActive
                  ? 'text-blue-700 scale-110'
                  : 'text-gray-400 hover:text-blue-600'
              }`}
            >
              {item.icon(isActive)}
              <span className={`text-[10px] font-medium ${isActive ? 'opacity-100' : 'opacity-70'}`}>
                {item.label}
              </span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
